 <h3>LLK BS (Loka Latihan Keterampilan Bridgestone)</h3>
<div align="justify">Sebuah tempat yang sering disebut dengan kawah condro dimuko. Tempat dimana kami ditempa fisiknya, dibentuk kepribadian dan kedisiplinanya, di cerdaskan otaknya, di upgrade skillnya. Ya itulah LLK BS (Loka Latihan Keterampilan Bridgestone). LLK BS adalah salah satu CSR PT bridgestone global di bidang pendidikan. Diklat yang didirikan pada tahun 1982 ini terletaknya di jalan bekasi raya km 27 harapan jaya, bekasi utara. Pada tahun 2016 LLK BS mendapat penghargaan sebagai salah satu CSR di bidang pendidikan dari bridgestone global.</div>
 <br>
 <img src="image/penghargaan.jpg"/>
 <div align="center"> penerimaan penghargaan oleh bpk moch mulia</div> 
 <p>
 <div align="justify"> Setiap tahun, 24 lulusan SMK terbaik diterima menjadi siswa didik baru di LLK BS, dari 24 siswa itu akan dibagi menjadi 4 kelas, 2 kelas jurusan elektrik dan 2 kelas jurusan mekanik. Materi yang diajarkan hampir sama dengan mata kuliah di perguruan tinggi. Siswa dididik oleh instruktur profesional sejumlah 7 orang. </div>
 
 <br>

<img src="image/gb_siswallkbs.jpg" />
<div align="center"> Kegiatan LLKBS</div>
<p>
<div align="justify"> 
Siswa yang diterima harus sanggup tinggal di asrama selama 2 tahun dan selama itu juga dilarang menikah. Sebelum tahun 2015 senioritas masih berlaku di asrama namun pada saat ini warga asrama hidup dengan damai. Selain ada siswa yang menjabat sebagai Ketua siswa yang urusanya dengan diklat di asrama ada juga ketua asrama yang masing masing bekerjasama untuk mengkoordinir di diklat dan asrama bersama pejabat lainya selama satusemester,</div>
<p>
<img src="image/sport.jpg" />
<div align="center"> Kegiatan Olahraga</div>
<p>
<div align="justify">LLK BS sangat menekankan siswanya untuk disiplin. Sebelum adzan sholat subuh berkumandang ada piket khusus untuk membangunkan seluruh warga asrama. Setelah sholat subuh para siswa mengerjakan piket harian yang sudah dibagi sedemikian rupa, Jam 07.00 kami harus sudah siap untuk lari pagi menyusuri komplek BS yang kini sudah tidak ada. Sebelum melaksanakan diklat pada pukul 8,15 para siswa dan instruktur melakukan taiso dan briefing bersama. Kegiatan diklat dilakukan dari jam 8,15 sampai jam 16.4. Porsi diklatnya pun terbalik dari perguruan pada umumnya, di LLK-BS 60% praktek dan 40% teori. Setelah belajar di diklat ada beberapa aktifitas rutin yang dijalankan. untuk hari selasa dan kamis ada olah raga bersama, biasanya futsal, volly, bulutangkis atau basket, selain itu pada hari rabu ada kajian rutin di masjid Bridgestone. Pada malam hari ada kegiatan diskusi yang dimulai dari jam 08,00 sampai jam 10.00. Begitu padat kegiatan siswa LLK BS bahkan tidak jarang beberapa siswa mengundurkan diri karena tidak kuat menjalani rutinitas yang begitu padat dan penuh tekanan.</div>
