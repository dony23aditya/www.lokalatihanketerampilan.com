<br >
<div align="justify">Didirikannya LLKBS ini adalah suatu bukti kepedulian PT Bridgestone Tire Indonesia terhadap tanggung jawab sosial untuk masyarakat atau CSR (Corporate Social Responsibility), khususnya dalam bidang pendidikan dan pelatihan calon teknisi. Sampai dengan Agustus tahun 2015 LLKBS telah menghasilkan lulusan sebanyak 707 alumnus, yang bekerja diberbagai perusahaan besar di Indonesia dan juga menjadi Entrepreneur.</div>   
<br >
<div align="justify">Demi membantu meningkatkan pengetahuan dan keterampilan lulusan Sekolah Menengah Kejuruan dalam rangka pengembangan dunia Iindustri di Indonesia, dengan alasan bahwa lulusan Sekolah Menengah kejuruan belum sepenuhnya siap untuk langsung terjun ke dunia industri, sehingga perlu pembekalan keterampilan teknik sehingga siap menjadi teknisi yang handal sesuai dengan perkembangan teknologi saat ini.</div>
